// function declaration
function soma(x, y) {
    return x + y
}
console.log(soma(5, 3))


// function expression
const sub = function (x, y) {
    return x - y
}
console.log(sub(5, 3))

// named function expression (não é muito usada)
const mult = function mult(x, y) {
    return x * y
}
console.log(mult(5, 3))